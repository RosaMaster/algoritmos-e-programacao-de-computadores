# Título: Função Média
# Descrição: Defina, diretamente no shell interativo, a função média(), que aceita dois
# números como entrada e retorna a média dos números. Um exemplo de uso é:
# >>> average(2, 3.5)
# 2.75

# Entrada
def media(a, b):
    return (a + b) / 2

# Processamento
print(media(2, 3.5))
print(media(2, 3))
print(media(2, 4))


# Teste
# >>> media(2, 3.5)
# 2.75
# >>> media(2, 3)
# 2.5
# >>> media(2, 4)
# 3.0

